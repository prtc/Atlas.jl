# Helper function to get data for specific ion (1-based indexing)
"""
    get_partition_data(ion_index::Int)

Get partition function data for a specific ion.

# Arguments
- `ion_index`: Ion index (1-374)

# Returns
- Array of 6 integers representing the partition function data for the ion

# Example
```julia
data = get_partition_data(1)  # Get data for H I
```
"""
function get_partition_data(ion_index::Int)
    if ion_index < 1 || ion_index > 374
        error("Ion index must be between 1 and 374, got $ion_index")
    end
    arr = get_nnn_array()
    return arr[:, ion_index]
end
