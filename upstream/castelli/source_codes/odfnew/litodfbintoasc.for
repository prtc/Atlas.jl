	PROGRAM RDFBINCD
        INTEGER*2 KAP(25,57,12)
	OPEN(UNIT=9,STATUS='OLD', FORM='UNFORMATTED',READONLY)
c	TYPE 1
c1	FORMAT(3X,'ENTER THE NUMBER OF THE FIRST RECORD TO BE PRINTED ',$)
c	ACCEPT*,IREC1
c	TYPE 2
c2	FORMAT(3X,'ENTER THE NUMBER OF THE LAST RECORD TO BE PRINTED ',$)
c	ACCEPT*,IREC2
	DO 7 IREC=1,1212
	   do 80 it=1,57
	READ(9,END=9)((KAP(ip,it,istep),istep=1,12),ip=1,25)
 80	continue
3	FORMAT(I5)
	DO 5 IT=1,57
	DO 5 IP=1,25
	WRITE(12,4)(KAP(IP,IT,ISTEP),ISTEP=1,12)
4	FORMAT(1X,12(I6,1X))
5	CONTINUE
7	CONTINUE
9	CALL EXIT
	END 
