      PROGRAM EH2OPARTRIDGEBIN
C     READS ENERGY LEVELS FROM PARTRIDGE AND SCHWENKE (1997) AND WRITES BINARY
      REAL*8 EH2O(273201)
      INTEGER*4 QUANTUMS(8)
      CHARACTER*8 JKK(273201),V1V2V3PS(273201)
C
C             E(CM-1)   P S  J KP KO V1 V2 V3
C161      1  3755.95509 1 1  0  0  0  0  0  1
C             E(CM-1)     J KP KO V1V2V3PS
C161      1  3755.95509   0  0  0  0 0 111
C     ISOTOPOMERS
C     161       1-170625
C     171  170626-200840
C     181  200841-231285
C     162  231286-273201
C
      READ(1,1)
      READ(1,1)
      READ(1,1)
      READ(1,1)
      DO 2 LEVEL=1,273201
      READ(1,1)EH2O(LEVEL),(QUANTUMS(J),J=1,8)
    1 FORMAT(10X,F12.5,2I2,6I3)
      WRITE(JKK(LEVEL),'(I2,I3,I3)')QUANTUMS(3),QUANTUMS(4),
     1 QUANTUMS(5)
      WRITE(V1V2V3PS(LEVEL),'(I2,I2,I2,I1,I1)')QUANTUMS(6),
     1 QUANTUMS(7),QUANTUMS(8),QUANTUMS(1),QUANTUMS(2)
    2 CONTINUE
      WRITE(7)EH2O,JKK,V1V2V3PS
      CALL EXIT
      END
