      PROGRAM RH2OIR
      REAL*8 EH2O(273201)
      CHARACTER*8 JKK(273201),V1V2V3PS(273201)
      REAL*4 XISO(4),X2ISO(4)
      CHARACTER*2 LABELISO(4)
C               1H1H16O 1H1H17O 1H1H18O 1H2H16O 
      DATA XISO/  .9976,  .0004,  .0020, .00001/                                                              
      DATA X2ISO/-0.001, -3.398, -2.690, -5.000/
      DATA LABELISO/'16','17','18','26'/
C
      READ(48)EH2O,JKK,V1V2V3PS
      LENGTHFILE=5952389
C     AT END ENERGY LEVEL DIFFERENCES ARE SMALLER THAN THE ENERGY LEVEL 
C     PRECISION IN EH2O (=0.00001CM-1)
      DO 7 ILINE=1,LENGTHFILE
      READ(11,1)IE,IEP,IGFLOG
    1 FORMAT(3I6)
      ISO=1
      IF(IE.LT.170626)GO TO 3
      ISO=2
      IF(IE.LT.200841)GO TO 3
      ISO=3
      IF(IE.LT.231286)GO TO 3
      ISO=4     
    3 GFLOG=(IGFLOG-16384)*.001
      WAVENO=ABS(EH2O(IE)-EH2O(IEP))
      IF(MOD(ILINE,1000).EQ.1.OR.ILINE.EQ.LENGTHFILE)
     1WRITE(6,2)ILINE,IE,IEP,LABELISO(ISO),WAVENO,GFLOG,
     2EH2O(IE),JKK(IE),V1V2V3PS(IE),EH2O(IEP),JKK(IEP),V1V2V3PS(IEP)
    2 FORMAT(1X,3I7,1X,A2,F10.5,F7.3,F12.5,1X,A8,1X,A8,
     1 F12.5,1X,A8,1X,A8)       
    7 CONTINUE
      CALL EXIT
      END
