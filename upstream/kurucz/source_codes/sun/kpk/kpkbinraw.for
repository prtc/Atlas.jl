      PROGRAM KPKCARD
C     CONVERTS RAW OR FINAL KPK CARDS TO BINARY
      DIMENSION Q(8)
      DO 9 I=1,88927
      READ(1,2)Q
    2 FORMAT(8F10.0)
      IF(MOD(I,1000).EQ.1)PRINT 3,I,Q
    3 FORMAT(I10,F10.4,F10.0,F10.4,F10.0,F10.4,F10.0,F10.4,F10.0)
      WRITE(9)Q(1),Q(2)
      WRITE(9)Q(3),Q(4)
      WRITE(9)Q(5),Q(6)
      IF(I.EQ.88927)CALL EXIT
    9 WRITE(9)Q(7),Q(8)
      END
