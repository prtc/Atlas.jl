
      PROGRAM INTERP
      IMPLICIT REAL*8 (A-H,O-Z)
      CHARACTER*80 A
      DIMENSION XMU(20),QMU(40),WLEDGE(200),TITLE(74)
      REAL*8 TEFF,GLOG,TITLE,WBEGIN,RESOLU,XMU,WLEDGE,RATIO
      REAL*8 QMU
CTEFF   5770.  GRAVITY 4.44000 LTE 
CTITLE MODEL C                                                                   
CFLUX    1  1000.00        2.997925E+14   9.0229E-06   9.0229E-06   1.00000
CFLUX 1991200000.00        1.498963E+12   8.8979E-10   8.8979E-10   1.00000
CFLUX
      READ(1)TEFF,GLOG,TITLE,WBEGIN,RESOLU,NWL,IFSURF,NMU,XMU,NEDGE,
     1WLEDGE
      WRITE(6,1010)TEFF,GLOG,TITLE
 1010 FORMAT(  5H TEFF,F7.0,7H   GRAV,F7.3/7H TITLE ,74A1)
      RATIO=1.+1./RESOLU
      WEND=WBEGIN*RATIO**(NWL-1)
      WRITE(6,1005)WBEGIN,WEND,RESOLU
 1005 FORMAT(2F12.5,F12.1,F12.5)
      I=0
      DO 157 IWL=1,NWL                                                 
      READ(1)FNU,CONT
      if(mod(iwl,100).ne.1)go to 157
      WL=WBEGIN*RATIO**(IWL-1)                                             
      FREQ=2.99792458E17/WL
      RESID=FNU/CONT
      I=I+1
      WRITE(7,411)I,WL,FREQ,FNU,CONT,RESID
  411 FORMAT(4HFLUX,I5,F9.2,1PE20.6,E13.4,E13.4,0PF10.5)
  157 CONTINUE
      CALL EXIT
      END
