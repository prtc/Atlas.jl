      PROGRAM SEPARATEDF
      INTEGER*4 IDFOUT(7)
      INTEGER*2 IDF(12),ISTEPS(12,21,56)
      EQUIVALENCE (IDFOUT(2),IDF(1))
      CHARACTER*13 NAMELIT(5),NAMEBIG(5)
      DATA NAMEBIG/'NEWBDFP00BIG0','NEWBDFP00BIG1','NEWBDFP00BIG2',
     1'NEWBDFP00BIG4','NEWBDFP00BIG8'/
      DATA NAMELIT/'NEWBDFP00LIT0','NEWBDFP00LIT1','NEWBDFP00LIT2',
     1'NEWBDFP00LIT4','NEWBDFP00LIT8'/
      DO 9 IVT=1,5
      OPEN(UNIT=2,NAME=NAMEBIG(IVT),FORM='UNFORMATTED',STATUS='NEW',
     1 RECORDTYPE='FIXED',RECL=7056)
      DO 5 NU=1,328
      DO 3 IP=1,21
      DO 3 IT=1,56
      READ(1)IDFOUT
      DO 3 ISTEP=1,12
    3 ISTEPS(ISTEP,IP,IT)=IDF(ISTEP)
    5 WRITE(2)ISTEPS
      CLOSE(UNIT=2)
      OPEN(UNIT=2,NAME=NAMELIT(IVT),FORM='UNFORMATTED',STATUS='NEW',
     1 RECORDTYPE='FIXED',RECL=7056)
      DO 8 NU=1,1212
      DO 7 IP=1,21
      DO 7 IT=1,56
      READ(1)IDFOUT
      DO 7 ISTEP=1,12
    7 ISTEPS(ISTEP,IP,IT)=IDF(ISTEP)
    8 WRITE(2)ISTEPS
      CLOSE(UNIT=2)
    9 CONTINUE
      CALL EXIT
      END
