      PROGRAM SPLIGF100
      CHARACTER*149 A
      CHARACTER*10 FILENAME
      REAL*8 WL
      DIMENSION CUTI(11)
      DATA CUTI/100.,150.,200.,300.,400.,500.,600.,800.,1200.,3000.,
     1 1.E7/
      CHARACTER*10 FILEI(11)
      DATA FILEI/'GF0100.100','GF0150.100','GF0200.100',
     1 'GF0300.100','GF0400.100','GF0500.100','GF0600.100',
     2 'GF0800.100','GF1200.100','GF3000.100','GFEND.100'/
      OPEN(UNIT=7,STATUS='OLD',RECL=160)
      ICUT=1
      CUT=CUTI(1)
      FILENAME=FILEI(1)
      OPEN(UNIT=8,STATUS='NEW',NAME=FILENAME,RECL=160)
      DO 20 LINE=1,9999999
      READ(7,'(F11.4,A149)',END=25)WL,A
      IF(WL.GE.CUT)THEN
      CLOSE(UNIT=8)
      PRINT 5,FILENAME,N
    5 FORMAT(1X,A10,I10,' LINES')
      ICUT=ICUT+1
      CUT=CUTI(ICUT)
      FILENAME=FILEI(ICUT)
      OPEN(UNIT=8,STATUS='NEW',NAME=FILENAME,RECL=160)
      N=0
      ENDIF
   11 WRITE(8,'(F11.4,A149)')WL,A
      N=N+1
   20 CONTINUE
   25 CLOSE(UNIT=8)
      PRINT 5,FILENAME,N
      FILENAME='TOTAL'
      PRINT 5,FILENAME,LINE-1
      CALL EXIT
      END
