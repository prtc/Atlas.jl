      PROGRAM SPLITGF10
      CHARACTER*149 A
      CHARACTER*10 FILENAME
      REAL*8 WL
      OPEN(UNIT=7,STATUS='OLD',RECL=160)
      NLINES=564950
      ICUT=10
      CUT=10.
      STEP=10.
      FILENAME='GF0010.10'
      OPEN(UNIT=8,STATUS='NEW',NAME=FILENAME,RECL=160)
      DO 20 LINE=1,999999
      READ(7,'(F11.4,A149)',end=25)WL,A
      IF(WL.GE.CUT)THEN
      CLOSE(UNIT=8)
      PRINT 5,FILENAME,N
    5 FORMAT(1X,A10,I10,' LINES')
      IF(CUT.EQ.1000.)STEP=100.
      IF(CUT.EQ.9900.)THEN
      CUT=1.E7
      FILENAME='GFEND.10'
      GO TO 11
      ENDIF
      CUT=CUT+STEP
      ICUT=CUT
      WRITE(FILENAME,10)ICUT
   10 FORMAT('GF',I4.4,'.10')
   11 OPEN(UNIT=8,STATUS='NEW',NAME=FILENAME,RECL=160)
      N=0
      ENDIF
      WRITE(8,'(F11.4,A149)')WL,A
      N=N+1
   20 CONTINUE
   25 CLOSE(UNIT=8)
      PRINT 5,FILENAME,N
      FILENAME='TOTAL'
      PRINT 5,FILENAME,LINE-1
      CALL EXIT
      END
