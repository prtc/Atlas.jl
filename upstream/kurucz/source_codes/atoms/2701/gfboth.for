      PROGRAM GFBOTH
C     MATCHES GF*.POS WITH GF*.LAB
C     ASSUMES FIRST AND LAST LINES ARE NOT LAB
      CHARACTER*160 LINE(30000)
    1 FORMAT(A160)
      DO 2 I=1,30000
    2 READ(1,1,END=3)LINE(I)
    3 N=I-1
      LINE(N+1)=' '
      DO 4 I=1,N
      IF(LINE(I)(99:99).NE.'N')GO TO 4
      IF(LINE(I-1)(25:80).EQ.LINE(I)(25:80))THEN
      WRITE(2,1)LINE(I-1)
      WRITE(2,1)LINE(I)
      GO TO 4
      ENDIF
      IF(LINE(I+1)(25:80).EQ.LINE(I)(25:80))THEN
      WRITE(2,1)LINE(I+1)
      WRITE(2,1)LINE(I)
      ENDIF
    4 CONTINUE
      CALL EXIT
      END
