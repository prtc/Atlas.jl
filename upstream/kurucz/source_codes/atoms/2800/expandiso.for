      PROGRAM EXPANDISO
C     ISOTOPICALLY SPLITS THE LINES IN GF2800.POS FOR WHICH THE ENERGY
C     LEVEL SPLITTINGS HAVE BEEN DETERMINED
      IMPLICIT REAL*8 (A-H,O-Z)
      CHARACTER*28 EVE,ODD,EIN(132)
      CHARACTER*74 MIDDLE
      CHARACTER*13 GFLOGCODE
      CHARACTER*8 XISO(5)
      REAL*8 EISO(5,132),EMEAN(132)
      READ(1,'(A28)')
      READ(1,'(A28,5F13.4)')(EIN(LEV),(EISO(ISO,LEV),ISO=1,5),LEV=1,132)
      XISO(1)='58-0.166'
      XISO(2)='60-0.583'
      XISO(3)='61-1.947'
      XISO(4)='62-1.444'
      XISO(5)='64-2.041'
      DO 77 LEV=1,132
   77 READ(EIN(LEV),'(F12.4)')EMEAN(LEV)
      OPEN(UNIT=2,STATUS='OLD',RECL=160)
      OPEN(UNIT=3,STATUS='NEW',RECL=160,CARRIAGECONTROL='LIST')
      DO 11 LINE=1,99999
      READ(2,3,END=13)WL,GFLOGCODE,EVE,ODD,MIDDLE,ISOSHIFT
    3 FORMAT(F11.4,A13,A28,A28,A74,I6)
      DO 4 LEVE=1,132
      IF(EVE.EQ.EIN(LEVE))GO TO 5
    4 CONTINUE
      GO TO 7
    5 DO 6 LEVO=1,132
      IF(ODD.EQ.EIN(LEVO))GO TO 9
    6 CONTINUE
    7 WRITE(3,3)WL,GFLOGCODE,EVE,ODD,MIDDLE,ISOSHIFT
      GO TO 11
    9 WLVAC=1.D7/ABS(EMEAN(LEVE)-EMEAN(LEVO))
      DO 10 ISO=1,5
      WLVACISO=1.D7/ABS(EISO(ISO,LEVE)-EISO(ISO,LEVO))
      ISOSHIFT=10000.D0*(WLVACISO-WLVAC)+.5D0
      WLISO=WL+ISOSHIFT*0.0001D0
      MIDDLE=MIDDLE(1:36)//XISO(ISO)//MIDDLE(45:74)
   10 WRITE(3,3)WLISO,GFLOGCODE,EVE,ODD,MIDDLE,ISOSHIFT
   11 CONTINUE
   13 CALL EXIT
      END
